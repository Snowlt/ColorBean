package colorpad.core;

/**
 * The Type of Color Formula
 * 表示配色方案的类型
 */
public enum FormulaType {
    /**
     * 同类色
     */
    Monochromatic,
    /**
     * 互补色
     */
    Complementary,
    /**
     * 分裂互补色
     */
    SplitComplementary,
    /**
     * 邻近色
     */
    Analogous,
    /**
     * 三角色
     */
    Triadic,
    /**
     * 四角色
     */
    Tetradic

}
